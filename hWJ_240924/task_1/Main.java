package de.telran.homeWorkJava.hWJ_240924.task_1;

public class Main {
    public static void main(String[] args) {

                // Примитивные типы данных
                char ch = 'G';
                int i = 89;
                byte b = 4;
                short s = 56;
                float f = 4.7333436f;
                double d = 4.355453532;
                long l = 12121L;

                // Обёрточные типы данных (не примитивные)
                Character chObj = 'G';
                Integer iObj = 89;
                Byte bObj = 4;
                Short sObj = 56;
                Float fObj = 4.7333436f;
                Double dObj = 4.355453532;
                Long lObj = 12121L;

                // Вывод примитивных типов данных
                System.out.println("char: " + ch);
                System.out.println("int: " + i);
                System.out.println("byte: " + b);
                System.out.println("short: " + s);
                System.out.println("float: " + f);
                System.out.println("double: " + d);
                System.out.println("long: " + l);
        System.out.println("---------------------------------");
                // Вывод обёрточных типов данных
                System.out.println("Character: " + chObj);
                System.out.println("Integer: " + iObj);
                System.out.println("Byte: " + bObj);
                System.out.println("Short: " + sObj);
                System.out.println("Float: " + fObj);
                System.out.println("Double: " + dObj);
                System.out.println("Long: " + lObj);
            }
        }

